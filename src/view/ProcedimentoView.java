    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        ProcedimentoModel procedimento = new ProcedimentoModel();
        if(jtxIdProcedimento.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Preencha o código do procedimento!"
                    , "Retorno Tela", JOptionPane.ERROR_MESSAGE);
        else{
            procedimento.setIdProcedimento(Integer.parseInt(jtxIdProcedimento.getText()));
            //CONTROLLER
            ProcedimentoController controller = new ProcedimentoController();
            procedimento = controller.selecionar(procedimento);
            //VALIDAR SE OBJETO FORNECEDOR FOI ENCONTRADO... 
            if(procedimento == null)
                JOptionPane.showMessageDialog(this, "Procedimento não encontrado!"
                    , "Retorno BD", JOptionPane.ERROR_MESSAGE);
            else{
                //PREENCHER OS CAMPOS...
                jtxNome.setText(procedimento.getNome());
                jtxValorPadrao.setText(String.valueOf(procedimento.getValorPadrao()));
                jtxDescricao.setText(procedimento.getDescricao());
                
                jbNovo.setEnabled(false);
                jbEditar.setEnabled(true);
                jbExcluir.setEnabled(true);
                jbSalvar.setEnabled(false);
                jbLimpar.setEnabled(true);
                jtxIdProcedimento.setEditable(false);
                jtxNome.setEditable(true);
                jtxDescricao.setEditable(true);
                jtxValorPadrao.setEditable(true);
            }
        }
    }//GEN-LAST:event_jbPesquisarActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        String nome = jtxNome.getText();
        String descricao = jtxDescricao.getText();
        String valorStr = jtxValorPadrao.getText();
        if((nome.isEmpty())||(descricao.isEmpty())||(valorStr.isEmpty()))
            JOptionPane.showMessageDialog(this, "Digite todos os campos!"
                    , "Retorno Tela", JOptionPane.ERROR_MESSAGE);
        else{
            float valorPadrao = Float.parseFloat(valorStr);
            ProcedimentoModel procedimento = new ProcedimentoModel();
            procedimento.setNome(nome);
            procedimento.setValorPadrao(valorPadrao);
            procedimento.setDescricao(descricao);
            //CONTROLLER
            ProcedimentoController controller = new ProcedimentoController();
            if(controller.inserir(procedimento)){
                JOptionPane.showMessageDialog(this, "Procedimento Inserido com sucesso!");
                limparCampos();
                inicializa();
                preencherTabela();
            }else
                JOptionPane.showMessageDialog(this, "Erro ao inserir o procedimento!"
                    , "Retorno BD", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

